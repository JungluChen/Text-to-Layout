"""Deterministic 5×7 stroke font — text labels as plain layer polygons.

GDS text records are annotation-only in many flows; a research tile's title and
port labels must survive as *geometry* so every viewer and mask writer renders
them identically. Each glyph is a 5×7 bitmap; horizontal runs of on-pixels merge
into single rectangles to keep polygon counts low.
"""

from __future__ import annotations

from textlayout.models import Polygon, rectangle

