GLYPH_COLS = 5
GLYPH_ROWS = 7
CHAR_ADVANCE = GLYPH_COLS + 1  # one blank column between characters


def supported_characters() -> str:
    return "".join(sorted(_GLYPHS))


def text_polygons(
    text: str,
    *,
    layer: str,
    origin: tuple[float, float],
    cell_um: float,
) -> tuple[Polygon, ...]:
    """Render ``text`` as rectangles; ``cell_um`` is one bitmap pixel in µm.

    Unsupported characters raise ``ValueError`` — silently dropping label
    characters would falsify what the mask says.
    """
    if cell_um <= 0:
        raise ValueError("cell_um must be positive")
    x0, y0 = origin
    polygons: list[Polygon] = []
    for index, char in enumerate(text.upper()):
        glyph = _GLYPHS.get(char)
        if glyph is None:
            raise ValueError(
                f"character {char!r} is not in the 5x7 label font "
                f"(supported: {supported_characters()!r})"
            )
        char_x = x0 + index * CHAR_ADVANCE * cell_um
        for row, bits in enumerate(glyph):
            y_top = y0 + (GLYPH_ROWS - row) * cell_um
            col = 0
            while col < GLYPH_COLS:
                if bits & (1 << (GLYPH_COLS - 1 - col)):
                    run = 1
                    while col + run < GLYPH_COLS and bits & (1 << (GLYPH_COLS - 1 - col - run)):
                        run += 1
                    polygons.append(
                        rectangle(
                            layer,
                            char_x + col * cell_um,
                            y_top - cell_um,
                            char_x + (col + run) * cell_um,
                            y_top,
                        )
                    )
                    col += run
                else:
                    col += 1
    return tuple(polygons)


def text_width_um(text: str, cell_um: float) -> float:
    if not text:
        return 0.0
    return (len(text) * CHAR_ADVANCE - 1) * cell_um


def text_height_um(cell_um: float) -> float:
    return GLYPH_ROWS * cell_um
