def em_geometry_correction(
    *,
    target_frequency_ghz: float,
    extracted_frequency_ghz: float,
    extracted_impedance_ohm: float | None = None,
    target_impedance_ohm: float = 50.0,
) -> dict[str, Any]:
    """Return first-order geometry seeds for the next EM-backed optimization iteration."""
    if target_frequency_ghz <= 0 or extracted_frequency_ghz <= 0:
        raise ValueError("Target and extracted frequencies must be positive")
    frequency_ratio = extracted_frequency_ghz / target_frequency_ghz
    result: dict[str, Any] = {
        "schema": "text-to-gds.em-geometry-correction.v1",
        "target_frequency_ghz": target_frequency_ghz,
        "extracted_frequency_ghz": extracted_frequency_ghz,
        "frequency_error_percent": 100.0
        * (extracted_frequency_ghz - target_frequency_ghz)
        / target_frequency_ghz,
        "recommended_cpw_length_scale": frequency_ratio,
        "recommended_capacitance_scale_if_inductance_fixed": frequency_ratio**2,
        "recommended_inductance_scale_if_capacitance_fixed": frequency_ratio**2,
        "validity": "First-order optimizer seed; rerun HFSS/Q3D after geometry regeneration.",
    }
    if extracted_impedance_ohm is not None:
        if extracted_impedance_ohm <= 0 or target_impedance_ohm <= 0:
            raise ValueError("Extracted and target impedances must be positive")
        result["extracted_impedance_ohm"] = extracted_impedance_ohm
        result["target_impedance_ohm"] = target_impedance_ohm
        result["recommended_cpw_gap_scale_seed"] = math.sqrt(
            target_impedance_ohm / extracted_impedance_ohm
        )
    return result
