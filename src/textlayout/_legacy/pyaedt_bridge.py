def numeric_matrix_summary(path):
    source = Path(path)
    if not source.exists():
        return None
    rows = []
    for row in csv.reader(source.open(encoding="utf-8", errors="ignore")):
        numeric = []
        for value in row:
            try:
                numeric.append(float(value.strip()))
            except ValueError:
                continue
        if len(numeric) >= 2:
            rows.append(numeric)
    width = max((len(row) for row in rows), default=0)
    square = [row[-width:] for row in rows if len(row) >= width]
    return {"numeric_rows": len(rows), "maximum_numeric_columns": width, "matrix": square}


