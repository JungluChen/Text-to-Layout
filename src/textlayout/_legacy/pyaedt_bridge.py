def create_ports(app, config):
    created = []
    z_bottom = -float(config["ports"]["sheet_below_metal_um"])
    sheet_height = float(config["ports"]["sheet_height_um"])
    gap = float(config["ports"]["cpw_gap_um"])
    for index, port in enumerate(config["ports"]["items"], start=1):
        x, y = [float(value) for value in port["center"]]
        conductor_width = max(float(port.get("width", 10.0)), 0.1)
        span = max(conductor_width + 6.0 * gap, conductor_width * 3.0)
        orientation = int(round(float(port.get("orientation") or 0.0))) % 360
        safe_name = "".join(ch if ch.isalnum() else "_" for ch in port["name"])
        if orientation in (0, 180):
            sheet = app.modeler.create_rectangle(
                "YZ", [x, y - span / 2.0, z_bottom], [span, sheet_height],
                name=f"PortSheet_{safe_name}",
            )
            line = [[x, y, 0.0], [x, y + 0.45 * span, 0.0]]
        else:
            sheet = app.modeler.create_rectangle(
                "XZ", [x - span / 2.0, y, z_bottom], [span, sheet_height],
                name=f"PortSheet_{safe_name}",
            )
            line = [[x, y, 0.0], [x + 0.45 * span, y, 0.0]]
        boundary = app.lumped_port(
            sheet.name,
            integration_line=line,
            impedance=50,
            name=port["name"] or f"Port{index}",
        )
        created.append(boundary.name)
    return created


def export_fields(app, config, imported_objects, setup_sweep):
    outputs = {}
    frequency = f'{config["analysis"]["frequency_ghz"]:g}GHz'
    quantities = {
        "electric_field": ("Mag_E", config["outputs"]["electric_field_png"], "volume"),
        "magnetic_field": ("Mag_H", config["outputs"]["magnetic_field_png"], "volume"),
        "current_density": ("Mag_Jsurf", config["outputs"]["current_density_png"], "surface"),
    }
    for label, (quantity, path, plot_kind) in quantities.items():
        try:
            if plot_kind == "surface":
                plot = app.post.create_fieldplot_surface(
                    imported_objects,
                    quantity,
                    setup=setup_sweep,
                    intrinsics={"Freq": frequency, "Phase": "0deg"},
                    plot_name=f"TextToGDS_{label}",
                )
            else:
                plot = app.post.create_fieldplot_volume(
                    imported_objects,
                    quantity,
                    setup=setup_sweep,
                    intrinsics={"Freq": frequency, "Phase": "0deg"},
                    plot_name=f"TextToGDS_{label}",
                )
            exported = plot.export_image(path, orientation="top", show_region=False) if plot else False
            outputs[label] = str(exported) if exported else None
        except Exception as exc:
            outputs[label] = None
            outputs[f"{label}_error"] = str(exc)
    return outputs


def summarize_touchstone(path):
    source = Path(path)
    if not source.exists():
        return None
    scale = 1.0
    data_format = "MA"
    rows = []
    for raw_line in source.read_text(encoding="utf-8", errors="ignore").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("!"):
            continue
        if line.startswith("#"):
            tokens = line[1:].upper().split()
            scale = {"HZ": 1e-9, "KHZ": 1e-6, "MHZ": 1e-3, "GHZ": 1.0}.get(tokens[0], 1.0)
            data_format = next((item for item in ("RI", "MA", "DB") if item in tokens), "MA")
            continue
        values = [float(value) for value in line.split()]
        if len(values) >= 9:
            rows.append(values)
    if not rows:
        return None

    def pair_to_complex(a, b):
        if data_format == "RI":
            return complex(a, b)
        phase = math.radians(b)
        magnitude = 10.0 ** (a / 20.0) if data_format == "DB" else a
        return cmath.rect(magnitude, phase)

    frequencies = [row[0] * scale for row in rows]
    s21 = [abs(pair_to_complex(row[3], row[4])) for row in rows]
    notch_index = min(range(len(s21)), key=s21.__getitem__)
    peak_index = max(range(len(s21)), key=s21.__getitem__)
    return {
        "points": len(rows),
        "frequency_start_ghz": frequencies[0],
        "frequency_stop_ghz": frequencies[-1],
        "s21_notch_frequency_ghz": frequencies[notch_index],
        "s21_notch_db": 20.0 * math.log10(max(s21[notch_index], 1e-30)),
        "s21_peak_frequency_ghz": frequencies[peak_index],
        "s21_peak_db": 20.0 * math.log10(max(s21[peak_index], 1e-30)),
    }


def eigenmode_results(app, setup_name):
    solution_name = f"{setup_name} : LastAdaptive"
    quantities = app.post.available_report_quantities(
        report_category="Eigenmode Parameters",
        display_type="Data Table",
        solution=solution_name,
    )
    result = {"available_quantities": quantities, "values": {}}
    if not quantities:
        return result
    data = app.post.get_solution_data(
        expressions=quantities,
        setup_sweep_name=solution_name,
        report_category="Eigenmode Parameters",
    )
    for expression in getattr(data, "expressions", quantities):
        try:
            result["values"][expression] = [float(value) for value in data.data_real(expression)]
        except Exception as exc:
            result["values"][expression] = {"error": str(exc)}
    try:
        result["primary_sweep_values"] = [float(value) for value in data.primary_sweep_values]
    except Exception:
        pass
    return result


