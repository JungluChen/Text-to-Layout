def _write_script(template: str, config_path: Path, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    rendered = template.replace("__CONFIG_PATH__", str(config_path.resolve()))
    output_path.write_text(rendered, encoding="utf-8")


def _execute_script(
    script_path: Path, *, solve: bool, timeout_seconds: int = 3600
) -> dict[str, Any]:
    command = [sys.executable, str(script_path), "--overwrite"]
    if solve:
        command.append("--solve")
    try:
        completed = subprocess.run(
            command, capture_output=True, text=True, check=False, timeout=timeout_seconds
        )
    except subprocess.TimeoutExpired as exc:
        stdout = exc.stdout.decode(errors="replace") if isinstance(exc.stdout, bytes) else (exc.stdout or "")
        stderr = exc.stderr.decode(errors="replace") if isinstance(exc.stderr, bytes) else (exc.stderr or "")
        return {
            "command": command,
            "returncode": None,
            "stdout": stdout,
            "stderr": stderr + f"\n[text-to-gds] AEDT script timed out after {timeout_seconds} s.",
        }
    return {
        "command": command,
        "returncode": completed.returncode,
        "stdout": completed.stdout,
        "stderr": completed.stderr,
    }


def write_pyaedt_project_bundle(
    gds_path: str | Path,
    *,
    config_path: str | Path,
    hfss_script_path: str | Path,
    q3d_script_path: str | Path,
    report_path: str | Path,
    hfss_project_path: str | Path,
    q3d_project_path: str | Path,
    sidecar_path: str | Path | None = None,
    process_path: str | Path | None = None,
    setup_frequency_ghz: float = 6.0,
    sweep_start_ghz: float = 1.0,
    sweep_stop_ghz: float = 12.0,
    sweep_points: int = 221,
    run: bool = False,
    solve: bool = False,
    run_hfss: bool = True,
    run_q3d: bool = True,
) -> dict[str, Any]:
    """Write HFSS/Q3D PyAEDT scripts and optionally execute them."""
    gds = Path(gds_path).resolve()
    config_file = Path(config_path)
    hfss_script = Path(hfss_script_path)
    q3d_script = Path(q3d_script_path)
    report_file = Path(report_path)
    hfss_project = Path(hfss_project_path)
    q3d_project = Path(q3d_project_path)
    stem = hfss_project.with_suffix("")
    outputs = {
        "hfss_project": str(hfss_project.resolve()),
        "q3d_project": str(q3d_project.resolve()),
        "hfss_results_json": str(stem.with_suffix(".hfss.results.json").resolve()),
        "q3d_results_json": str(stem.with_suffix(".q3d.results.json").resolve()),
        "eigenmode_json": str(stem.with_suffix(".eigenmode.json").resolve()),
        "touchstone_s2p": str(stem.with_suffix(".s2p").resolve()),
        "q3d_matrix_csv": str(stem.with_suffix(".q3d.matrix.csv").resolve()),
        "electric_field_png": str(stem.with_suffix(".Efield.png").resolve()),
        "magnetic_field_png": str(stem.with_suffix(".Hfield.png").resolve()),
        "current_density_png": str(stem.with_suffix(".current_density.png").resolve()),
    }
    config = build_pyaedt_config(
        gds,
        outputs=outputs,
        sidecar_path=sidecar_path,
        process_path=process_path,
        setup_frequency_ghz=setup_frequency_ghz,
        sweep_start_ghz=sweep_start_ghz,
        sweep_stop_ghz=sweep_stop_ghz,
        sweep_points=sweep_points,
    )
    config_file.parent.mkdir(parents=True, exist_ok=True)
    config_file.write_text(json.dumps(config, indent=2), encoding="utf-8")
    _write_script(HFSS_SCRIPT, config_file, hfss_script)
    _write_script(Q3D_SCRIPT, config_file, q3d_script)

    result: dict[str, Any] = {
        "schema": "text-to-gds.pyaedt-project.v1",
        "status": "prepared",
        "backend": "Ansys Electronics Desktop through PyAEDT 1.1+",
        "source_gds": str(gds),
        "config_path": str(config_file),
        "hfss_script_path": str(hfss_script),
        "q3d_script_path": str(q3d_script),
        "expected_outputs": outputs,
        "capabilities": [
            "HFSS driven-modal S-parameters",
            "HFSS eigenmode frequency and Q data",
            "HFSS electric, magnetic, and surface-current field images",
            "Q3D Maxwell and coupling capacitance matrices",
        ],
        "review_gates": list(config["validity"].values()),
    }
    if run:
        returncodes = []
        if run_hfss:
            result["hfss_execution"] = _execute_script(hfss_script, solve=solve)
            returncodes.append(result["hfss_execution"]["returncode"])
        if run_q3d:
            result["q3d_execution"] = _execute_script(q3d_script, solve=solve)
            returncodes.append(result["q3d_execution"]["returncode"])
        result["status"] = (
            "executed" if returncodes and all(code == 0 for code in returncodes) else "execution_failed"
        )
    report_file.parent.mkdir(parents=True, exist_ok=True)
    report_file.write_text(json.dumps(result, indent=2), encoding="utf-8")
    result["report_path"] = str(report_file)
    return result


